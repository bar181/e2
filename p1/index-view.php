    <h2>E2 Project 1 by Bradley Ross</h2>

    <h2>Goal of Each Round</h2>
    <ul>
        <li>Closest card values to 21 win the round</li>
        <li>Card values: Cards 2-10 = value of card, Ace = 1 or 11, Face cards = 10</li>
    </ul>

    <h2>Mechanics</h2>
    <ul>
        <li>Player and Dealer start with $100</li>
        <li>Player and Dealer bet $10 each round</li>
        <li>Player and Dealer are dealt 2 cards each from a shuffled deck</li>
        <li>Player may add additional cards to their hand</li>
        <li>If Player card values exceeds 21, Dealer wins bets</li>
        <li>If Player card values equal 21, Player wins bets</li>
        <li>Dealer adds cards when values are 16 or less and stops at 17 or more</li>
        <li>If Dealer values exceed 21, Player wins bets</li>
        <li>Highest card values wins bet, ties returns original bets</li>
        <li>Rounds continue one person has all the money</li>
    </ul>


